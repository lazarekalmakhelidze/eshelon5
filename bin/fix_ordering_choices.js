async function fixChoices() {
    try {
        const batch = db.batch();

        for (const update of updates) {
            const ref = db.collection('questions').doc(update.id);
            batch.update(ref, {
                question_text: update.question_text,
                choice_a: update.choice_a,
                choice_b: update.choice_b,
                choice_c: update.choice_c,
                choice_d: update.choice_d
            });
            console.log(`Prepared update for ${update.id}`);
        }

        await batch.commit();
        console.log('Successfully fixed choices for ordering questions.');

        process.exit(0);
    } catch (e) {
        console.error(e);
        process.exit(1);
    }
}

fixChoices();
