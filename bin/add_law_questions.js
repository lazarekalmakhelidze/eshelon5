async function addQuestions() {
    try {
        const snapshot = await db.collection('questions').orderBy('id', 'desc').limit(1).get();
        let startId = 1000;
        if (!snapshot.empty) {
            const maxId = parseInt(snapshot.docs[0].id) || parseInt(snapshot.docs[0].data().id);
            if (!isNaN(maxId)) {
                startId = maxId + 1;
            }
        }
        
        // However, I want to use UUIDs or string IDs just to be safe. Let's use string of startId.
        const batch = db.batch();
        
        questions.forEach((q, index) => {
            const docId = (startId + index).toString();
            const docRef = db.collection('questions').doc(docId);
            
            const fullQuestion = {
                id: docId,
                ...q,
                subject: 'กพ ภาค ก',
                category: 'กฎหมาย',
                catalogs: ['กฎหมาย'],
                skill: 'กฎหมาย',
                exam_set: 'ข้อสอบจริง กพ พิเศษ (19 กพ 66)',
                exam_year: '2566',
                explanation: 'ข้อสอบจริง กพ ภาค ก ปี 66 (สอบพิเศษ 19 ก.พ. 66)',
                created_at: new Date().toISOString(),
                updated_at: new Date().toISOString(),
                difficulty: 50,
                rating: 0,
                ratingCount: 0
            };
            
            batch.set(docRef, fullQuestion);
        });
        
        await batch.commit();
        console.log(`Successfully added ${questions.length} questions starting from ID ${startId}`);
        process.exit(0);
    } catch (e) {
        console.error(e);
        process.exit(1);
    }
}

addQuestions();
